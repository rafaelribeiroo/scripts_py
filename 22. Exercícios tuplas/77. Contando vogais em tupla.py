# 77. Crie um programa que tenha uma tupla com várias palavras (não usar
# acentos). Depois disso, você deve mostrar, para cada palavra, quais
# são as suas vogais.
palavras = (
    'aprender', 'programar', 'linguagem', 'python',
    'curso', 'gratis', 'estudar', 'praticar',
    'trabalhar', 'mercado', 'programador', 'futuro'
)
print(f'Na palavra {palavras[0].upper()} temos')
while 'a' in palavras[0]:
	print('a')
while 'e' in palavras[0]:
	print('e')
while 'i' in palavras[0]:
	print('i')
while 'o' in palavras[0]:
	print('o')
while 'u' in palavras[0]:
	print('u')
