# 5. Faça um programa que leia um número inteiro e mostre na tela o seu
# sucessor e antecessor

n = int(input('Entre com um número: '))
print(f'Analisando o valor {n}, seu antecessor é {n-1} e o sucessor é {n+1}')
# Porque dessa forma e não usar 3 variáveis?
# Quanto menos você utilizar variáveis, mais memória você vai economizar do seu
# dispositivo.
